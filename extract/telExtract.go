func exportAreaCode(tel string) (areaCode string, area string) {
	if strings.Contains(tel, "-") {
		areaCode = strings.Split(tel, "-")[0]
		area = telAreaCodeMap[areaCode]
		return
	}
	if strings.Contains(tel, ")") {
		areaCode = strings.Replace(strings.Split(tel, ")")[0], "(", "", -1)
		area = telAreaCodeMap[areaCode]
		return
	}
	if strings.Contains(tel, "）") {
		areaCode = strings.Replace(strings.Split(tel, "）")[0], "（", "", -1)
		area = telAreaCodeMap[areaCode]
		return
	}
	return
}
